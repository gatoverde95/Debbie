# Idioma actual con un valor predeterminado
current_language = "en"

# Función para establecer el idioma
def set_language(lang_code):
    global current_language
    if lang_code in translations:
        current_language = lang_code

# Función de traduccióndef _(key, **kwargs):
def _(key, **kwargs):
    value = translations[current_language].get(key, key)
    if isinstance(value, str):
        return value.format(**kwargs)
    return value  # Devuelve el valor sin formatear si no es una cadena